package org.bolikov.pattern;

public class Person {

    private Long id;
    private String login;
    private String password;

    public Person(Long id, String login, String password) {
        this.id = id;
        this.login = login;
        this.password = password;
    }

    public Long getId() {
        return id;
    }

    public String getLogin() {
        return login;
    }

    public String getPassword() {
        return password;
    }
}
